import json

def remove_ids_from_json(
    json_path,
    ids_to_remove,
    id_field="original_scenario_id"
):
    # Load the JSON data
    with open(json_path, "r", encoding="utf-8") as f:
        data = json.load(f)

    # Filter out objects with IDs in the removal list
    filtered = [
        obj for obj in data
        if obj.get(id_field) not in ids_to_remove
    ]

    print(f"Removed {len(data) - len(filtered)} objects. {len(filtered)} remain.")

    # Overwrite the file with the filtered data
    with open(json_path, "w", encoding="utf-8") as f:
        json.dump(filtered, f, indent=2, ensure_ascii=False)

